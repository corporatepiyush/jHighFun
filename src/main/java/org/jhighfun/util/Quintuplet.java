package org.jhighfun.util;

public class Quintuplet<F, S, T, FO, FI> {

    public F _1;
    public S _2;
    public T _3;
    public FO _4;
    public FI _5;

    public Quintuplet(F first, S second, T third, FO fourth, FI fifth) {
        this._1 = first;
        this._2 = second;
        this._3 = third;
        this._4 = fourth;
        this._5 = fifth;
    }
}