package org.jhighfun.util;


public class Quadruplet<F, S, T, FO> {

    public F _first;
    public S _second;
    public T _third;
    public FO _fourth;

    public Quadruplet(F first, S second, T third, FO fourth) {
        this._first = first;
        this._second = second;
        this._third = third;
        this._fourth = fourth;
    }
}
